N, M = map(int, input().split())
arr = [x+1 for x in range(N)]
for x in range(M):
    i, j = map(int, input().split())
    arr = arr[:i-1] + arr[i-1:j][::-1] + arr[j:]
    
print(' '.join([str(x) for x in arr]))