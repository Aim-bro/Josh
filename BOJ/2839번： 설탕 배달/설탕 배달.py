N = int(input())

kg5 = N // 5
while kg5 >= 0:
    remain = N - (kg5 * 5)
    if remain % 3 == 0:
        kg3 = remain // 3
        print(kg5 + kg3)
        break
    kg5 -= 1
else:
    print(-1)




